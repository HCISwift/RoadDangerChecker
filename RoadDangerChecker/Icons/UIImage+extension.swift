import UIKit

// MARK: Static Icons
extension UIImage {
    enum Icon {
        static let danger = UIImage(named: "Danger")
        static let dangerSelected = UIImage(named: "DangerSelected")
        static let setting = UIImage(named: "Setting")
        static let road = UIImage(named: "Road")
        static let roadSelected = UIImage(named: "RoadSelected")
        static let cancel = UIImage(named: "Cancel")
        static let roadMarker = UIImage(named: "roadMarkerImage")
    }
}
